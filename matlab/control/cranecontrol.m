loop1 = feedback(C1*Mx, 1);

%------------------------------------------------------------------------------
% X(s) Control
%------------------------------------------------------------------------------
% rlocus(loop1 * Grx*rrx * 1/s) => >10
C2 = 125;
loop2 = feedback(C2 * loop1 * Grx*rrx * (1/s), 1);

%------------------------------------------------------------------------------
% W(s) Control
%------------------------------------------------------------------------------
% rlocus(-loop2*W) shows that proportional control is not good enough!
% The minimum overshoot obtainable is ~40% => Sucks...
C3 = 15;
loop3 = feedback(C3 * (-loop2) * W,  1);
loopd = feedback(     (-loop2) * W, C3);

